    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {                                              
        int resposta = JOptionPane.showConfirmDialog(rootPane, "Tem certeza? Essa ação alterará o valor do BD.");

        if (resposta == JOptionPane.YES_OPTION) {
            String novoValorTexto = jTextFieldNovoValor.getText().trim();
            if (!novoValorTexto.isEmpty()) {
                try {
                    double novoValor = Double.parseDouble(novoValorTexto);
                    if (novoValor >= 1000) {
                        ServicoController controller = new ServicoController();
                        String tipoServ = (String) jComboBoxTipoServ.getSelectedItem();
                        boolean sucesso = controller.alterarServico(tipoServ, novoValor);

                        if (sucesso) {
                            JOptionPane.showMessageDialog(null, "Valor alterado com sucesso");
                        } else {
                            JOptionPane.showMessageDialog(null, "Erro ao alterar o valor do serviço");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "O novo valor deve ser maior ou igual a 1000.", "Erro", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(null, "Por favor, insira um valor numérico válido.", "Erro", JOptionPane.ERROR_MESSAGE);
                } catch (ExceptionDAO ex) {
                    JOptionPane.showMessageDialog(null, "Erro: " + ex.toString());
                    Logger.getLogger(GerenciarServicos.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Por favor, insira um valor.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
        	JOptionPane.showMessageDialog(null, "Ação cancelada.", "Cancelado",JOptionPane.INFORMATION_MESSAGE);
        }
    }


