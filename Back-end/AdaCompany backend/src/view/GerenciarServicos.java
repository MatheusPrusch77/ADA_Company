    private void jComboBoxTipoServActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxTipoServActionPerformed

        try {
            ServicoController controller = new ServicoController();
            String tipoServ = jComboBoxTipoServ.getSelectedItem().toString();
            Servico serv = controller.consultaServico(tipoServ);
            jTextFieldValorAtual.setText(Double.toString(serv.getValor()));
        } catch (ExceptionDAO ex) {
            JOptionPane.showMessageDialog(null, "Erro na consulta do tipo de serviço: " + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jComboBoxTipoServActionPerformed

